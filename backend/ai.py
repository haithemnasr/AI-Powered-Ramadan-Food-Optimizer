import os
import google.generativeai as genai
from dotenv import load_dotenv

# Load environment variables
load_dotenv()

# Configure Google Gemini API
genai.configure(api_key=os.getenv("GEMINI_API_KEY"))

# Initialize the model
model = genai.GenerativeModel('gemini-2.5-flash')

def generate_suggestions(today, leftovers):
    """
    Generate AI-powered food suggestions using Google Gemini API (FREE!)
    Tailored for Tunisian Ramadan cooking and waste reduction
    """
    
    # Create the prompt for Gemini
    prompt = f"""أنت مساعد ذكي متخصص في المطبخ التونسي ورمضان. مهمتك مساعدة العائلات التونسية على تقليل هدر الطعام في رمضان بطريقة عملية وبالدارجة التونسية.

المعلومات المتوفرة:
- الماكلة اللي تحضّرت اليوم: {today if today else "ما ذكرش"}
- الفضلات الباقية: {leftovers if leftovers else "ما ذكرش"}

⚠️ قواعد مهمّة برشا لازم تتبعها:

1) **ما تعاودش نفس الماكلة الرئيسية اللي طيّبناها البارح.**
   • لو كان حضّرنا كسكسي → غدوة ما نقترحوش كسكسي.
   • لو كان حضّرنا عدس → غدوة نقترحو مرقة، مقرونة، طاجين… أي حاجة مختلفة.
   • العائلات التونسية ما تحبّش تعاود نفس الماكلة يومين وراء بعض.

2) **الباقى ما تستعملهاش كطبق رئيسي أبدًا.**
   • استعملها كان كـ: مقبلات، سلاطة محسّنة، طبق جانبي، إضافة خفيفة، حشوة، ولا حاجة صغيرة تتاكل مع الطبق الرئيسي.
3) **الطبق الرئيسي لازم يكون جديد تمامًا ومتنوع.**
   • مثال: طاجين، مقرونة، شوربة، مرقة جلبانة، بيتزا تونسية، عجة، بريكة معمّرة، سلطة مشوية، إلخ.
4) **خليك عملي، مختصر، ومباشر.**
   • 200–300 كلمة كافية.
   • استعمل الدارجة التونسية الطبيعية: شنوة، بش، برشا، كيفاش، إلخ.
   • استعمل إيموجي مناسب.
   • لا تخترع أطباق جديدة أو غير موجودة في المطبخ التونسي
   •  اقترح فقط وصفات تونسية تقليدية حقيقية وموجودة
   •  أعطي وصفات كاملة والكميات المستعملة للأكلة و طريقة التحضير

5) **قدّم 4 نقاط رئيسية:**
   1.  أكلات مقترحة لغدوة: طبق رئيسي جديد + كيفاش نستعمل الباقي متع المأكولات.
   2. تنبيه لو فما خطر هدر (كمية كبيرة، ما يصبرش في الثلاجة…).
   3. نصائح للحفظ والتخزين بطريقة عملية.
   4. تقدير تقريبي لقدّاش من الماكلة تنجم تتنجّم من الهدرة.

ابدأ مباشرة بالاقتراحات، بلا مقدمات رسمية."""


    try:
        # Call Gemini API
        response = model.generate_content(prompt)
        
        # Extract the response text
        response_text = response.text
        
        return response_text
        
    except Exception as e:
        # Fallback response in case of API error
        return f"""⚠️ عذراً، صار خطأ في الاتصال بالذكاء الاصطناعي.

🔧 الرجاء التأكد من:
- مفتاح API صحيح (GEMINI_API_KEY في ملف .env)
- الاتصال بالإنترنت متوفر

📝 في الانتظار، هاذي بعض النصائح العامة:
• حطّو الفضلات في علب محكمة في الثلاجة
• سخّنو الماكلة مليح قبل ما تاكلوها
• استعملو الفضلات في مدة 2-3 أيام

خطأ تقني: {str(e)}"""